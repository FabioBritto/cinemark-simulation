package com.cinema.lanchonete;

public class Salgados {

	private static String nomeSalgado;
	private static double precoSalgado;
	
	public Salgados(String nomeSalgado, double precoSalgado) {
		this.nomeSalgado = nomeSalgado;
		this.precoSalgado = precoSalgado;
	}
	
	String getNomeSalgado() {
		return this.nomeSalgado;	
	}
	
	double getPrecoSalgado() {
		return this.precoSalgado;
	}
}
